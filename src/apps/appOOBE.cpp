static AppOOBE app;
void AppOOBE::waitClick()
{
    while (hal.btnl.isPressing() == false && hal.btnr.isPressing() == false && hal.btnc.isPressing() == false)
        delay(10);
}
void AppOOBE::setup()
{
    bool ret = false;
    int reti = 0;
    int stage = hal.pref.getInt("oobe", 0);
    if(stage == 3)
    {
        if(GUI::msgbox_yn("", "OOBE") == false)
        {
            appManager.goBack();
            return;
        }
    }
    else if(stage == 1)
    {
        goto stage1;
    }
    else if(stage == 2)
    {
        goto stage2;
    }
    display.clearScreen();
    display.drawXBitmap(0, 0, OOBE_Start_bits, 296, 128, 0);
    display.display();
    waitClick();
    display.clearScreen();
    display.drawXBitmap(0, 0, OOBE_checkWiring_bits, 296, 128, 0);
    display.display(true);
    hal.pref.putInt("oobe", 1);
    peripherals.check();
stage1:
    display.clearScreen();
    display.drawXBitmap(0, 0, OOBE_BG_bits, 296, 128, 0);
    display.display(true);
    ret = GUI::msgbox_yn("", "");
    if (ret)
        goto jmp_tutorial;
    while (ret == false)
    {
        ret = GUI::msgbox_yn("", "", "", "");
    }
    while (ret == true)
    {
        ret = GUI::msgbox_yn("", "", "", "");
    }
    while (reti == 0)
    {
        reti = GUI::msgbox_number("", 3, 0);
        GUI::msgbox("", (String("") + String(reti)).c_str());
    }
jmp_tutorial:
stage2:
    display.clearScreen();
    display.drawXBitmap(0, 0, OOBE_BG_bits, 296, 128, 0);
    u8g2Fonts.drawUTF8(40, 85, "");
    display.display(true);
    hal.pref.putInt("oobe", 2);
    if(config[PARAM_CLOCKONLY] == "1" && hal.pref.getInt("oobe", 0) == 2)
    {
        display.clearScreen();
        u8g2Fonts.drawUTF8(95, 100, "...");
        display.display(true);
        hal.pref.putString(SETTINGS_PARAM_HOME_APP, "clock");
        hal.pref.putInt("oobe", 3);
        ESP.restart();
    }
    hal.autoConnectWiFi();
    u8g2Fonts.drawUTF8(40, 100, "NTP");
    display.display(true);
    NTPSync();
    u8g2Fonts.drawUTF8(95, 100, "...");
    display.display(true);
    hal.pref.putString(SETTINGS_PARAM_HOME_APP, "clock");
    hal.pref.putInt("oobe", 3);
    ESP.restart();
}